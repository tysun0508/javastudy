class Ex07{
      public static void main(String [] args){
         // 대입연산자 : 특정한 상수값이나 변수값 또는 객체를
         //               변수에 전달하여 기억 시킬때 사용
         // a= b => 변수 a에 데이터값 b를 기억시킨다.(저장)
         // a += 10 ; => 변수a가 가지고 있는 데이터와 10을 더한후
         //              다시 변수 a에 저장한다.


           int su = 10 ;
           su += 10 ;
           System.out.println("su = "+su);

          // su를 1씩 증가하는 방법
           ++su ;
           su++;
           su += 1;
           su = su + 1 ;


   }
}