class Ex03{
     public static void main(String[] args){
             // 산술연산자 : +, -, *, /, %(나머지)
            int su1 = 9 ;
            int su2 = 4 ;
            int result = 0 ;
            
            result = su1 + su2 ;
            System.out.println(su1 + "+" + su2 + "=" +result);

            result = su1 - su2 ;
            System.out.println(su1 + "-" + su2 + "=" +result);

            result = su1 * su2 ;
            System.out.println(su1 + "*" + su2 + "=" +result);

            result = su1 / su2 ;
            System.out.println(su1 + "/" + su2 + "=" +result);

            result = su1 % su2 ;
            System.out.println(su1 + "%" + su2 + "=" +result);

            

    }

}